package src.com.certicom.certiscan.utils;

import java.util.Date;

public class DateBean {
	
	private String month;
	private String year;
	private Date fechaSelect;
	private String fechaString;
	
	private Date periodo;
	
	private double monto_factura =0.0;
	private double monto_planilla =0.0;
	
	private Integer criterio;
	private String des_criterio;
	
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public Date getFechaSelect() {
		return fechaSelect;
	}
	public void setFechaSelect(Date fechaSelect) {
		this.fechaSelect = fechaSelect;
	}
	public String getFechaString() {
		return fechaString;
	}
	public void setFechaString(String fechaString) {
		this.fechaString = fechaString;
	}
	public Date getPeriodo() {
		return periodo;
	}
	public void setPeriodo(Date periodo) {
		this.periodo = periodo;
	}
	public double getMonto_factura() {
		return monto_factura;
	}
	public void setMonto_factura(double monto_factura) {
		this.monto_factura = monto_factura;
	}
	public double getMonto_planilla() {
		return monto_planilla;
	}
	public void setMonto_planilla(double monto_planilla) {
		this.monto_planilla = monto_planilla;
	}
	public Integer getCriterio() {
		return criterio;
	}
	public void setCriterio(Integer criterio) {
		this.criterio = criterio;
	}
	public String getDes_criterio() {
		return des_criterio;
	}
	public void setDes_criterio(String des_criterio) {
		this.des_criterio = des_criterio;
	}

	
	

}
