	public void setDes_ciclo(String des_ciclo) {
		this.des_ciclo = des_ciclo;
	}

	public String getDesTipoBono() {
		return desTipoBono;
	}

	public void setDesTipoBono(String desTipoBono) {
		this.desTipoBono = desTipoBono;
	}
	
