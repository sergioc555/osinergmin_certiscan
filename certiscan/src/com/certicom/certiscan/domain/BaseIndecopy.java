package com.certicom.certiscan.domain;

import java.util.Date;

public class BaseIndecopy {

	private Integer baseIndecopyId;
	private String codigo;
	private String valor;
	private String fecharegistro;
	private String tiporegistro;
	private Date periodo;
	private Date fechacarga;
	private Integer idCabeceraIndecopy;
	private Boolean nuevo;
	
	private String motivo;
	private String detalle;
	


	public BaseIndecopy(){;}


	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

	public String getValor() {
		return valor;
	}

	public void setValor(String valor) {
		this.valor = valor;
	}


	public Integer getBaseIndecopyId() {
		return baseIndecopyId;
	}


	public void setBaseIndecopyId(Integer baseIndecopyId) {
		this.baseIndecopyId = baseIndecopyId;
	}


	public String getFecharegistro() {
		return fecharegistro;
	}


	public void setFecharegistro(String fecharegistro) {
		this.fecharegistro = fecharegistro;
	}


	public String getTiporegistro() {
		return tiporegistro;
	}


	public void setTiporegistro(String tiporegistro) {
		this.tiporegistro = tiporegistro;
	}
	public Date getPeriodo() {
		return periodo;
	}


	public void setPeriodo(Date periodo) {
		this.periodo = periodo;
	}


	public Date getFechacarga() {
		return fechacarga;
	}


	public void setFechacarga(Date fechacarga) {
		this.fechacarga = fechacarga;
	}


	public Boolean getNuevo() {
		return nuevo;
	}


	public void setNuevo(Boolean nuevo) {
		this.nuevo = nuevo;
	}


	public String getMotivo() {
		return motivo;
	}


	public void setMotivo(String motivo) {
		this.motivo = motivo;
	}


	public String getDetalle() {
		return detalle;
	}


	public void setDetalle(String detalle) {
		this.detalle = detalle;
	}


	public Integer getIdCabeceraIndecopy() {
		return idCabeceraIndecopy;
	}


	public void setIdCabeceraIndecopy(Integer idCabeceraIndecopy) {
		this.idCabeceraIndecopy = idCabeceraIndecopy;
	}



	
}
