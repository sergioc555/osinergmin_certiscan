package com.certicom.certiscan.domain;

import java.io.Serializable;
import java.util.Date;

public class CabeceraBaseIndecopy implements Serializable {

	private static final long serialVersionUID = 1L;
	
	private Integer idCabeceraIndecopy;
	private Integer idUsuario;
	private Date fecha;
	private Integer numeroRegistro;
	private Date periodo;
	private String nombreArchivo;
	
	//Transitorias
	private String nombreUsuario;
	
	public CabeceraBaseIndecopy(){;}

	public Integer getIdCabeceraIndecopy() {
		return idCabeceraIndecopy;
	}

	public void setIdCabeceraIndecopy(Integer idCabeceraIndecopy) {
		this.idCabeceraIndecopy = idCabeceraIndecopy;
	}

	public Integer getIdUsuario() {
		return idUsuario;
	}

	public void setIdUsuario(Integer idUsuario) {
		this.idUsuario = idUsuario;
	}

	public Date getFecha() {
		return fecha;
	}

	public void setFecha(Date fecha) {
		this.fecha = fecha;
	}

	public Integer getNumeroRegistro() {
		return numeroRegistro;
	}

	public void setNumeroRegistro(Integer numeroRegistro) {
		this.numeroRegistro = numeroRegistro;
	}

	public Date getPeriodo() {
		return periodo;
	}

	public void setPeriodo(Date periodo) {
		this.periodo = periodo;
	}

	public String getNombreUsuario() {
		return nombreUsuario;
	}

	public void setNombreUsuario(String nombreUsuario) {
		this.nombreUsuario = nombreUsuario;
	}

	public String getNombreArchivo() {
		return nombreArchivo;
	}

	public void setNombreArchivo(String nombreArchivo) {
		this.nombreArchivo = nombreArchivo;
	}

}
