package com.certicom.certiscan.domain;

import java.util.List;

public class CabeceraFacturacion {
	private Integer idCabeceraFacturacion;
	private String descripcion;
	private Boolean estado;
	private String propiedad;
	private String columna;
	
	//TRANSITORIAS
	private List<String> nombresCoincidentes;
	
	public Integer getIdCabeceraFacturacion() {
		return idCabeceraFacturacion;
	}
	public void setIdCabeceraFacturacion(Integer idCabeceraFacturacion) {
		this.idCabeceraFacturacion = idCabeceraFacturacion;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public Boolean getEstado() {
		return estado;
	}
	public void setEstado(Boolean estado) {
		this.estado = estado;
	}
	public String getPropiedad() {
		return propiedad;
	}
	public void setPropiedad(String propiedad) {
		this.propiedad = propiedad;
	}
	public List<String> getNombresCoincidentes() {
		return nombresCoincidentes;
	}
	public void setNombresCoincidentes(List<String> nombresCoincidentes) {
		this.nombresCoincidentes = nombresCoincidentes;
	}
	public String getColumna() {
		return columna;
	}
	public void setColumna(String columna) {
		this.columna = columna;
	}

	
}
