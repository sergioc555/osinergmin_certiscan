package com.certicom.certiscan.domain;

import java.util.Date;

public class Lote {
	
	private Integer id_lote;
	private String cod_lote;
	private Date fecha_registro;
	private Integer cantidad_expedientes;
	private String estado;
	private Integer filtradopor;
	private Date fec_ini;
	private Date fec_fin;
	private String vfiltradopor;
	private String entregado_a;

	
	public Lote(){
		
	}

	public Integer getId_lote() {
		return id_lote;
	}

	public void setId_lote(Integer id_lote) {
		this.id_lote = id_lote;
	}

	public String getCod_lote() {
		return cod_lote;
	}

	public void setCod_lote(String cod_lote) {
		this.cod_lote = cod_lote;
	}

	public Date getFecha_registro() {
		return fecha_registro;
	}

	public void setFecha_registro(Date fecha_registro) {
		this.fecha_registro = fecha_registro;
	}

	public Integer getCantidad_expedientes() {
		return cantidad_expedientes;
	}

	public void setCantidad_expedientes(Integer cantidad_expedientes) {
		this.cantidad_expedientes = cantidad_expedientes;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public Integer getFiltradopor() {
		return filtradopor;
	}

	public void setFiltradopor(Integer filtradopor) {
		this.filtradopor = filtradopor;
	}

	public Date getFec_ini() {
		return fec_ini;
	}

	public void setFec_ini(Date fec_ini) {
		this.fec_ini = fec_ini;
	}

	public Date getFec_fin() {
		return fec_fin;
	}

	public void setFec_fin(Date fec_fin) {
		this.fec_fin = fec_fin;
	}

	public String getVfiltradopor() {
		return vfiltradopor;
	}

	public void setVfiltradopor(String vfiltradopor) {
		this.vfiltradopor = vfiltradopor;
	}

	public String getEntregado_a() {
		return entregado_a;
	}

	public void setEntregado_a(String entregado_a) {
		this.entregado_a = entregado_a;
	}
	
}
