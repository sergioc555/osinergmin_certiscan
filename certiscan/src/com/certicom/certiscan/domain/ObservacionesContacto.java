package com.certicom.certiscan.domain;

public class ObservacionesContacto {

	private Integer observacioncontactoid;
	private Integer id_indicadores_call;
	private String observacion;

	private IndicadorCall indicadorCall;


	public Integer getObservacioncontactoid() {
		return observacioncontactoid;
	}


	public void setObservacioncontactoid(Integer observacioncontactoid) {
		this.observacioncontactoid = observacioncontactoid;
	}


	public Integer getId_indicadores_call() {
		return id_indicadores_call;
	}


	public void setId_indicadores_call(Integer id_indicadores_call) {
		this.id_indicadores_call = id_indicadores_call;
	}


	public String getObservacion() {
		return observacion;
	}


	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}


	public IndicadorCall getIndicadorCall() {
		return indicadorCall;
	}


	public void setIndicadorCall(IndicadorCall indicadorCall) {
		this.indicadorCall = indicadorCall;
	}
	
}
