package com.certicom.certiscan.domain;

import java.math.BigDecimal;

public class DetalleEntidad {

	private String nombreEntidad;
	private String codigoEmpresa;
	private String calificacion;
	private BigDecimal creditoVigente;
	private BigDecimal creditoRefinanciado;
	private BigDecimal creditoVencido;
	private BigDecimal creditoCobroJudicial;
	private String tipoMoneda;
	
	public String getNombreEntidad() {
		return nombreEntidad;
	}
	public void setNombreEntidad(String nombreEntidad) {
		this.nombreEntidad = nombreEntidad;
	}
	public String getCalificacion() {
		return calificacion;
	}
	public void setCalificacion(String calificacion) {
		this.calificacion = calificacion;
	}
	public BigDecimal getCreditoVigente() {
		return creditoVigente;
	}
	public void setCreditoVigente(BigDecimal creditoVigente) {
		this.creditoVigente = creditoVigente;
	}
	public BigDecimal getCreditoRefinanciado() {
		return creditoRefinanciado;
	}
	public void setCreditoRefinanciado(BigDecimal creditoRefinanciado) {
		this.creditoRefinanciado = creditoRefinanciado;
	}
	public BigDecimal getCreditoVencido() {
		return creditoVencido;
	}
	public void setCreditoVencido(BigDecimal creditoVencido) {
		this.creditoVencido = creditoVencido;
	}
	public BigDecimal getCreditoCobroJudicial() {
		return creditoCobroJudicial;
	}
	public void setCreditoCobroJudicial(BigDecimal creditoCobroJudicial) {
		this.creditoCobroJudicial = creditoCobroJudicial;
	}
	public String getTipoMoneda() {
		return tipoMoneda;
	}
	public void setTipoMoneda(String tipoMoneda) {
		this.tipoMoneda = tipoMoneda;
	}
	public String getCodigoEmpresa() {
		return codigoEmpresa;
	}
	public void setCodigoEmpresa(String codigoEmpresa) {
		this.codigoEmpresa = codigoEmpresa;
	}
	
	
}
