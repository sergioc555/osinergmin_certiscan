package com.certicom.certiscan.domain;

public class ExpedienteRequisito {
	
	private Integer id;
	private Integer cod_expediente;
	private Integer cod_requisito;
	private boolean estado;
	
	private String desRequisito;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getCod_expediente() {
		return cod_expediente;
	}
	public void setCod_expediente(Integer cod_expediente) {
		this.cod_expediente = cod_expediente;
	}
	public Integer getCod_requisito() {
		return cod_requisito;
	}
	public void setCod_requisito(Integer cod_requisito) {
		this.cod_requisito = cod_requisito;
	}
	public boolean isEstado() {
		return estado;
	}
	public void setEstado(boolean estado) {
		this.estado = estado;
	}
	public String getDesRequisito() {
		return desRequisito;
	}
	public void setDesRequisito(String desRequisito) {
		this.desRequisito = desRequisito;
	}
	
	
	
}
